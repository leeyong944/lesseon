package test;

public class Calculator1 {
	int sum(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}
	int subtract(int num1, int num2) {
		int result = num1 - num2;
		return result;
	}
	int multiply(int num1, int num2) {
		int result = num1 * num2;
		return result;
	}
	int divide(int num1, int num2) {
		int result = num1 / num2;
		return result;
	}

}
